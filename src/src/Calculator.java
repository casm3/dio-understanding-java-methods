import java.util.Scanner;

public class Calculator {

    protected int sum(int num1, int num2) {
        return num1 + num2;
    }

    protected int subtraction(int num1, int num2) {
        return num1 - num2;
    }

    protected float division(float num1, float num2) {
        return num1 / num2;
    }

    protected int multiplication(int num1, int num2) {
        return num1 * num2;
    }
}
