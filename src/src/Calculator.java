import java.util.Scanner;

public class Calculator {

    protected static int sum(int num1, int num2) {
        return num1 + num2;
    }

    protected static int subtraction(int num1, int num2) {
        return num1 - num2;
    }

    protected static float division(float num1, float num2) {
        return num1 / num2;
    }

    protected static int multiplication(int num1, int num2) {
        return num1 * num2;
    }
}
